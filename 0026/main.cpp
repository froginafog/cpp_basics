#include <iostream>

using namespace std;

int main()
{
    int a[5];  //create an array which can store 5 integers

    a[0] = 10;
    a[1] = 20;
    a[2] = 30;
    a[3] = 40;
    a[4] = 50;

    cout << "a[0]: " << a[0] << endl;
    cout << "a[1]: " << a[1] << endl;
    cout << "a[2]: " << a[2] << endl;
    cout << "a[3]: " << a[3] << endl;
    cout << "a[4]: " << a[4] << endl;

    return 0;
}

