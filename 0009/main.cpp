#include <iostream>

using namespace std;

int main()
{
    int a;  //create variable of the type "int" and call it "a"
    int b;  //create variable of the type "int" and call it "b"

    a = 8;  //assign the value 8 to "a"
    b = 2;  //assign the value 2 to "b"

    int difference;  //create variable of the type "int" and call it "difference"

    difference = a - b;  //subtract the value stored in "b" from the value stored in "a"

    cout << "difference: " << difference << endl;

    return 0;
}

/*
difference: 6
*/
