#include <iostream>

using namespace std;

int main()
{
    int a;  //create variable of the type "int" and call it "a"
    int b;  //create variable of the type "int" and call it "b"

    a = 8;  //assign the value 8 to "a"
    b = 2;  //assign the value 2 to "b"

    int product;  //create a variable of the type "int" and call it "product"

    product = a * b;  //multiply the value stored in "a" by the value stored in "b"

    cout << "product: " << product << endl;

    return 0;
}

/*
product: 16
*/
