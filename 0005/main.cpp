#include <iostream>

using namespace std;


int main()
{
    float pi;
    //create a variable of the type "float" and call it "pi"
    //"float" means floating point number
    //examples of floating point numbers: 1.23, 345.671, -52.74

    pi = 3.14159;
    //assign the value 3.14159 to the variable "pi"

    cout << "pi: " << pi << endl;

    return 0;
}

/*
pi: 3.14159
*/
