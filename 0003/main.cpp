#include <iostream>

using namespace std;


int main()
{
    int number;
    //create a variable of the type "int" and call it "number"
    //"int" means integer
    //integers are whole numbers: ..., -2, -1, 0, 1, 2, ...

    number = 10;
    //assign the value 10 to the variable "number"

    cout << "number: " << number << endl;
    //print "number: " and the value stored in "number" to the screen

    return 0;  //return the value 0 to main()
}

/*
number: 10
*/
